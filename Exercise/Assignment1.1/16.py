# Different ways to clear a list in Python

# By using clear() method.
# l = [2, 6, 8, 2, 7, 34, 98]
# l.clear()
# print(l)
# ______________________________________________________________________________________________________________________

# By using del() method
# l = [2, 6, 8, 2, 7, 34, 98]
# del l[:]
# print(l)
# ______________________________________________________________________________________________________________________

# By using *= 0 method
# l = [2, 6, 8, 2, 7, 34, 98]
# l *= 0
# print(l)
