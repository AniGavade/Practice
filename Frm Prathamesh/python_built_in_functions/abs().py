# The abs() function returns the absolute value of the given number. If the number is a complex number,
# abs() returns its magnitude.

# For integers - integer absolute value is returned
print(abs(-10))
print(abs(10))

# For floating numbers - floating absolute value is returned
print(abs(-3.3))
print(abs(3.3))

# For complex numbers - magnitude of the number is returned
print(abs(5 - 5j))
print(abs(5 + 5j))

